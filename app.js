let a = false;

$("#btn_difference").click(function() {

	if (a === false) {

		$(".keyboard").hide(250)
		$(".mouse").toggle(250)
		$("#btn_difference").html("Try keyboard")

		a = true;

	} else {

		$(".keyboard").toggle(250)
		$(".mouse").hide(250)
		$("#btn_difference").html("Try mouse")

		a = false;

	}

})

